//Bitwise Operators: First convert the values in binary then do the Operations.
#include<iostream>
using namespace std;

int main(){
    int a=63,b= 33,ans1;
    ans1= a&b;//Binary AND
    int c=110, d=99, ans2;
    ans2= c|d;//Binary OR
    cout<<"The value of the ans1: "<<ans1<<endl;
    cout<<"The value of the ans2: "<<ans2<<endl;
return 0;
}